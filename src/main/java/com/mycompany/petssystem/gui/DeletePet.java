    private void jButtonDeletePetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletePetActionPerformed
        // TODO add your handling code here:
        
        try {
            int id = Integer.parseInt(txtDeleteIDPet.getText());
            int confirm = JOptionPane.showConfirmDialog(null, "¿Desea eliminar la mascota?");
            
            if(confirm==0){
                byte newStatus = 0;
            
                Pet pet = new Pet();
                pet.setId(id);
                pet.setStatus(newStatus);
                boolean result = petRepository.deletePet(pet);
            
                if(result){
                    JOptionPane.showMessageDialog(null, "La mascota fue eliminada");
                    txtDeleteIDPet.setText("");
                }else{
                    JOptionPane.showMessageDialog(null, "La mascota no fue eliminada");
                }
             
            }
            

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El ID debe ser un numero");
        }
    }//GEN-LAST:event_jButtonDeletePetActionPerformed


